    public MyBuckets(ArrayList<BucketModel> buckets) {
        initComponents();
        appearance();

        this.buckets = buckets;

        JTableHeader cabecalho = bucketTable.getTableHeader();
        cabecalho.setDefaultRenderer(new HeaderRenderer(bucketTable));

        DefaultTableModel modelo = (DefaultTableModel) bucketTable.getModel();

        int cont = 1;
        for (BucketModel b : buckets) {
            modelo
                    .addRow(new Object[]{
                cont,
                b.getName(),
                b.getFiles().size(),
                FileSize.getInstance().formatFileSize(b.getSize())
            });
            cont++;
        }

        if (bucketTable.getRowCount() > 0) {
            bucketTable.setDefaultRenderer(Object.class, new CellRender());
        }
    }

